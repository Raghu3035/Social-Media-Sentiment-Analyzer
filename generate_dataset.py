def generate_dataset(output_path: str, target_per_class: int = 350):
    """Generate a balanced CSV dataset with comments for each sentiment class."""
    rows = []

    # Expand each list to target size by repeating/sampling
    def expand(lst, n):
        result = []
        while len(result) < n:
            result.extend(lst)
        return random.sample(result, n)

    pos_comments = expand(POSITIVE_COMMENTS, target_per_class)
    neg_comments = expand(NEGATIVE_COMMENTS, target_per_class)
    neu_comments = expand(NEUTRAL_COMMENTS, target_per_class)

    for comment in pos_comments:
        rows.append(comment)
    for comment in neg_comments:
        rows.append(comment)
    for comment in neu_comments:
        rows.append(comment)

    random.shuffle(rows)

    os.makedirs(os.path.dirname(output_path), exist_ok=True)

    with open(output_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(["id", "comment"])
        for i, comment in enumerate(rows, start=1):
            writer.writerow([i, comment])

    print(f"✅ Dataset generated: {output_path}")
    print(f"   Total rows: {len(rows)}")
    print(f"   Positive: {target_per_class}, Negative: {target_per_class}, Neutral: {target_per_class}")


if __name__ == "__main__":
    out = Path(__file__).parent / "dataset" / "social_media_comments.csv"
    generate_dataset(str(out), target_per_class=350)
